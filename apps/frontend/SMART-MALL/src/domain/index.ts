/**
 * ============================================================================
 * 领域层 - 统一导出 (index.ts)
 * ============================================================================
 * 
 * 【文件职责】
 * 作为 Domain 层的统一入口，导出所有领域相关的模块。
 * 其他模块只需要从 '@/domain' 导入，不需要知道具体的文件路径。
 * 
 * 【Domain 层概述】
 * Domain 层是智慧商城系统的业务核心，负责管理商城的语义实体和业务逻辑。
 * 它是连接 Engine 层（纯渲染）和 Protocol 层（行为协议）的桥梁。
 * 
 * 【设计原则】
 * 1. 语义驱动：所有操作都基于业务语义（如"导航到店铺"而非"移动相机"）
 * 2. 领域隔离：不关心用户身份、不关心操作来源（UI/AI/系统）
 * 3. 单一职责：每个子模块只负责一个领域概念
 * 4. 可测试性：纯业务逻辑，不依赖 DOM 或浏览器 API
 * 
 * 【核心约束】
 * - 不关心用户是谁（权限检查在 Permission 模块）
 * - 不关心操作来源（UI/AI/系统都使用相同的接口）
 * - 只保证"语义正确性"（业务规则的正确执行）
 * 
 * 【模块说明】
 * 
 * 1. scene（场景模块）
 *    - 场景类型定义（Vector3D、BoundingBox 等）
 *    - 场景工具函数
 *    - 与 Engine 层的桥接
 * 
 * 2. mall（商城模块）
 *    - 商城实体（Mall、Floor、Area、Store）
 *    - 商城结构管理
 *    - 商城数据模型
 * 
 * 3. user（用户模块）
 *    - 用户实体（User、Role）
 *    - 用户上下文管理
 *    - 用户状态
 * 
 * 4. permission（权限模块）
 *    - 权限模型（Capability、Permission）
 *    - 权限检查逻辑
 *    - 区域授权管理
 * 
 * 5. registry（注册表模块）
 *    - 语义对象注册表
 *    - 对象 ID 到实体的映射
 *    - 快速查找和索引
 * 
 * 6. behaviors（行为模块）
 *    - 领域行为处理器
 *    - Action 到业务逻辑的映射
 *    - 行为执行和结果返回
 * 
 * 7. events（事件模块）
 *    - 领域事件定义
 *    - 事件发布/订阅
 *    - 跨模块通信
 * 
 * 8. factory（工厂模块）
 *    - 语义对象工厂
 *    - 实体创建和初始化
 *    - 复杂对象的构建
 * 
 * 9. loader（加载器模块）
 *    - 数据加载器
 *    - 从 API 加载商城数据
 *    - 数据转换和验证
 * 
 * 【与其他模块的关系】
 * - protocol/：Domain 层接收 Action，返回 DomainResult
 * - engine/：Domain 层调用 Engine 层进行 3D 渲染
 * - stores/：Store 层调用 Domain 层执行业务逻辑
 * - views/：视图层通过 Store 间接使用 Domain 层
 * 
 * 【数据流】
 * ```
 * Action (Protocol) → Behavior (Domain) → Engine (渲染) → Result (Protocol)
 * ```
 * 
 * 【使用示例】
 * ```typescript
 * import { 
 *   MallEntity, 
 *   UserContext, 
 *   PermissionChecker,
 *   NavigationBehavior 
 * } from '@/domain'
 * 
 * // 创建商城实体
 * const mall = new MallEntity(mallData)
 * 
 * // 检查权限
 * const canEdit = PermissionChecker.check(user, 'EDIT_STORE', storeId)
 * 
 * // 执行导航行为
 * const result = await NavigationBehavior.navigateToStore(storeId)
 * ```
 * 
 * ============================================================================
 */

