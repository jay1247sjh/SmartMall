/**
 * ============================================================================
 * 组件层 - 统一导出 (index.ts)
 * ============================================================================
 * 
 * 【文件职责】
 * 作为 Components 层的统一入口，导出所有可复用的 Vue 组件。
 * 其他模块只需要从 '@/components' 导入，不需要知道具体的文件路径。
 * 
 * 【Components 层概述】
 * Components 层包含智慧商城系统的所有可复用 UI 组件。
 * 这些组件遵循 Element Plus 设计规范，支持主题定制。
 * 
 * 【设计原则】
 * 1. 单一职责：每个组件只负责一个 UI 功能
 * 2. 可复用性：组件应该是通用的，可在多个页面使用
 * 3. 可组合性：小组件可以组合成大组件
 * 4. 无状态优先：优先使用 props 和 events，避免内部状态
 * 
 * 【组件分类】
 * 
 * 1. layouts（布局组件）
 *    - 页面布局框架
 *    - 侧边栏、头部、内容区
 *    - 响应式布局支持
 * 
 * 2. shared（共享组件）
 *    - 通用 UI 组件
 *    - 按钮、输入框、卡片等
 *    - 跨模块复用
 * 
 * 3. auth（认证组件）
 *    - 登录/注册相关组件
 *    - 表单输入、按钮、提示
 *    - 社交登录、打字机效果
 * 
 * 【与其他模块的关系】
 * - views/：视图层使用这些组件构建页面
 * - stores/：组件通过 props 接收数据，通过 events 触发状态更新
 * - styles/：组件使用全局样式变量和 SCSS 混入
 * 
 * 【使用示例】
 * ```typescript
 * import { AuthLayout, AuthInput, AuthButton } from '@/components'
 * 
 * // 在模板中使用
 * <AuthLayout>
 *   <AuthInput v-model="username" placeholder="用户名" />
 *   <AuthButton @click="handleLogin">登录</AuthButton>
 * </AuthLayout>
 * ```
 * 
 * ============================================================================
 */

