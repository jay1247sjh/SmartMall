/**
 * ============================================================================
 * 渲染引擎层 - 统一导出 (index.ts)
 * ============================================================================
 * 
 * 【文件职责】
 * 作为 Engine 层的统一入口，导出所有渲染引擎相关的模块。
 * 其他模块只需要从 '@/engine' 导入，不需要知道具体的文件路径。
 * 
 * 【Engine 层概述】
 * Engine 层是智慧商城系统的 3D 渲染核心，基于 Three.js 构建。
 * 
 * 【设计原则】
 * 1. 纯渲染层：不包含任何业务逻辑（不认识"商城""店铺"等概念）
 * 2. 可替换性：理论上可以替换为其他渲染引擎（如 Babylon.js）
 * 3. 高性能：按需渲染、对象池、材质复用等优化
 * 4. 易用性：封装 Three.js 复杂性，提供简洁的 API
 * 
 * 【模块说明】
 * 
 * 1. ThreeEngine（核心引擎）
 *    - 场景（Scene）管理
 *    - 渲染器（Renderer）管理
 *    - 相机（Camera）管理
 *    - 渲染循环（Animation Loop）
 *    - 资源清理（Dispose）
 * 
 * 2. camera（相机控制）
 *    - OrbitController: 轨道控制器，适合建模器场景
 *    - CameraController: 第三人称跟随控制器，适合漫游场景
 * 
 * 3. interaction（交互检测）
 *    - RaycasterManager: 射线检测，用于鼠标点击 3D 对象
 *    - SceneEventEmitter: 场景事件发射器，统一事件处理
 * 
 * 4. effects（视觉效果）
 *    - HighlightEffect: 高亮效果，用于选中、悬停状态
 * 
 * 5. materials（材质管理）
 *    - MaterialManager: 材质管理器，支持材质复用和缓存
 * 
 * 6. objects（对象管理）
 *    - GeometryFactory: 几何体工厂，创建各种形状
 *    - ObjectPool: 对象池，复用 3D 对象提升性能
 * 
 * 【与其他模块的关系】
 * - domain/scene/：领域层使用 Engine 层渲染 3D 场景
 * - domain/mall/：商城领域对象通过 Engine 层显示
 * - views/：视图层创建 ThreeEngine 实例并挂载到 DOM
 * 
 * 【使用示例】
 * ```typescript
 * import { ThreeEngine, type CameraMode } from '@/engine'
 * 
 * // 创建引擎实例
 * const engine = new ThreeEngine(containerElement, {
 *   backgroundColor: 0x87ceeb,
 *   antialias: true,
 *   cameraMode: 'orbit'
 * })
 * 
 * // 启动渲染循环
 * engine.start()
 * 
 * // 添加辅助网格
 * engine.addGridHelper(50, 50)
 * 
 * // 组件卸载时销毁
 * engine.dispose()
 * ```
 * 
 * ============================================================================
 */

